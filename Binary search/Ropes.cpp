#include <iostream>
#include <vector>
using namespace std;

bool good(double x, const vector<int>& a, int k) {
    int cnt = 0;
    for (int ln : a) {
        cnt += static_cast<int>(ln / x);
    }
    return cnt >= k;
}

int main() {
    int n, k;
    cin >> n >> k;

    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }

    double l = 0, r = 1e7 + 1;
    

    for (int i = 0; i < 100; ++i) {
        double m = (l + r) / 2;
        if (good(m, a, k)) {
            l = m;
        } else {
            r = m;
        }
    }


    cout <<static_cast<int> (l) << endl;

    return 0;
}