def binary_search(arr, x):
    l = 0
    r = len(arr) - 1
    if abs(arr[l] - x) < abs(arr[r] - x):
        num = arr[l]
    else:
        num = arr[r]
    while l <= r:
        m = (l + r) // 2
        if arr[m] == x:
            num = x
            return num
        elif arr[m] < x:
            l = m + 1
        else:
            r = m - 1

        if abs(arr[m] - x) < abs(num - x):
            num = arr[m]
        if abs(arr[m] - x) == abs(num - x):
            if num > arr[m]:
                num = arr[m]

    return num


