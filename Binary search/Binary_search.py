N, K = map(int, input().split())

arr_1 = [int(x) for x in input().split()]
arr_2 = [int(x) for x in input().split()]


def binary_search(arr, x):
    l = 0
    r = len(arr) - 1
    while l <= r:
        m = (l + r) // 2
        if arr[m] == x:
            return True
        elif arr[m] < x:
            l = m + 1
        else:
            r = m - 1
    return False


for x in arr_2:
    flag = binary_search(arr_1, x)
    if flag:
        print('YES')
    else:
        print('NO')

