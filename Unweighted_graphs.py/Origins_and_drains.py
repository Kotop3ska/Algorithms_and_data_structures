n = int(input())
matrix = []
for _ in range(n):
    row = [int(x) for x in input().split()]
    matrix.append(row)

ist = []
st = []

for i in range(n):
    flag_ist = True
    flag_st = True
    for j in range(n):
        if matrix[j][i] != 0:
            flag_ist = False
        if matrix[i][j] != 0:
            flag_st = False

    if flag_ist:
        ist.append(i + 1)
    if flag_st:
        st.append(i + 1)


print(len(ist))
for i in range(len(ist)):
    print(ist[i])
print(len(st))
for i in range(len(st)):
    print(st[i])